package org.example;

public class Calculator {

    public Double add(Double a, Double b) {
        return a + b;
    }

    public Double subtract(Double a, Double b) {
        return a - b;
    }

    public Double mult(Double a, Double b) {
        return a * b;
    }

    public Double div(Double a, Double b) {
        return a / b;
    }
}
